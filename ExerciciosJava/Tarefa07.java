package javaintroducao;

public class Tarefas8Java {
    public static void main(String[] args) {
        double hrs, segs, min;
        segs = 180;
        min = segs / 60;
        hrs = min / 60;
        System.out.println("Tempo em segundos " + segs);
        System.out.println("Tempo em minutos " + min);
        System.out.println("Tempo em horas  " + hrs);
    }
}