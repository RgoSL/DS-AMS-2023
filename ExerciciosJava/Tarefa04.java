package javaintroducao;

public class Tarefas5Java {
    public static void main(String[] args) {
        int id, aa, an, ac, ac2;
        aa = 2023;
        ac = 2050;
        ac2 = 2100;
        an = 2007;
        id = aa - an;
        ac = ac - an;
        ac2 = ac2 - an;
        System.out.println("Sua idade atual é " + id);
        System.out.println("Sua idade em 2050 será " + ac);
        System.out.println("Sua idade em 2100 será " + ac2);

        System.out.println("");
    }
}