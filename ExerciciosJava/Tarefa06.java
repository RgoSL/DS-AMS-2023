package javaintroducao;

public class Tarefas7Java {
    public static void main(String[] args) {
        double cent, deci, mili, metr;
        cent = 150;
        deci = cent / 10;
        mili = cent * 10;
        metr = cent / 100;
        System.out.println("O valor em centimetros é " + cent);
        System.out.println("O valor em decimetros é " + deci);
        System.out.println("O valor em milimetros é " + mili);
        System.out.println("O valor em metros é " + metr);
    }
}